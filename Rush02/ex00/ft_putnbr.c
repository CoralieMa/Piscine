#include "libr2.h"

void	ft_putnbr2(int nb)
{
	char	c;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
	{
		ft_putnbr2(nb / 10);
	}
	c = nb % 10 + '0';
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	write(1, "[", 1);
	ft_putnbr2(nb);
	write(1, "]", 1);
}
